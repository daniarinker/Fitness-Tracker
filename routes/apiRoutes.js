const router = require("express").Router();
const mongoose = require("mongoose");
const Workout = require("../models");

router.post("/api/exercises", (req, res) => {
  Workout.create(req.body)
      .then((dbWorkout) => {
      res.json(dbWorkout);
    })
    .catch((err) => {
      res.status(400).json(err);
    });
});

